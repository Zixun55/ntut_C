#include <stdio.h>
#include <math.h>
int con(long long n){//二進制轉十進制
    int d = 0, i = 0, r;
    while (n!=0){
        r = n%10;
        n /= 10;
        d += r*pow(2,i);
        ++i;
    }
    return d;
}
int C_m(int n,int time){
    if (n == 0 || n == 1) return time;
    else if (n%2 == 0) time = 1 + C_m(n/2,time);
    else if (n%2 != 0) time = 1+ C_m((n+1)/2,time);
    else return time;
}
